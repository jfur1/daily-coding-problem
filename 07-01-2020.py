# Describe and give an example of each of the following types of polymorphism:

# Ad-hoc polymorphism
# Parametric polymorphism
# Subtype polymorphism
# -----------------------------------

# 1. Ad-hoc Polymorphism:
#   The term refers to functions that can be applied to arguments of different
# types, but behave differently depending on the type of argument to which
# they are applied.


# 2. Parametric Polymorphism:
#   The term refers to functions that allow data types to be written generically,
# so that is can handle values uniformly without depending on their type.


# 3. Subtype Polymorphism:
#   Also called inclusion polymorphism, this restricts the range of types that can
# be used in a particular case of polymorphism.